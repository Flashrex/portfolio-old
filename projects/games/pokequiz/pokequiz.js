//Creating Table
const container = document.getElementById("container");

function makeRows(rows, cols) {
    container.style.setProperty('--grid-rows', rows);
    container.style.setProperty('--grid-cols', cols);
    for (c = 0; c < (rows * cols); c++) {
        let cell = document.createElement("div");
        cell.innerText = Pokemon[c].Nummer +".";
        container.appendChild(cell).className = "grid-item";
        cell.id = Pokemon[c].Deutsch;
    };
};

const columns = 5;
makeRows(Pokemon.length/columns, columns);

//Logic
let timer = undefined;
let startTime = "";
let points = 0;

function checkInput(input) {
    let pokemon = Pokemon.find(p => input.toLowerCase().includes(p.Deutsch.toLowerCase()));
    if(pokemon !== undefined && !pokemon.Found) {

        //Save Pokemon
        document.getElementById(pokemon.Deutsch).innerText = `${pokemon.Nummer}. ${pokemon.Deutsch}`;
        pokemon.Found = true;
        document.getElementById('inputfield').value = "";

        points++;
        document.getElementById("points").innerText = `${points}/151`;

        if(points === Pokemon.length) endGame();
    }

    if(input.toLowerCase() === "nidoran") {
        let pokemon =  Pokemon.find(p => p.Deutsch === "Nidoran♀")

        document.getElementById('Nidoran♀').innerText = `${pokemon.Nummer}. Nidoran♀`;
        pokemon.Found = true;

        pokemon =  Pokemon.find(p => p.Deutsch === "Nidoran♂")
        document.getElementById('Nidoran♂').innerText = `${pokemon.Nummer}. Nidoran♂`;
        pokemon.Found = true;

        document.getElementById('inputfield').value = "";

        points += 2;
        document.getElementById("points").innerText = `${points}/151`;
    }
}

function startGame() {
    if(timer === undefined) {
        //Timer starten
        startTime = Date.now(); //Genaue Startzeit für Bestenliste
        let seconds = 0;

        resetGame();

        toggleElements(true);

        timer = setInterval(() => {
            seconds++;
            let mins = Math.floor(seconds/60);
            let secs = Math.floor(seconds%60);
            document.getElementById('timer').innerText = `${mins < 10 ? "0" + mins : mins}:${secs < 10 ? "0" +secs : secs}`;

            if(seconds === 1200) {
                //20 Minuten
                clearInterval(timer);
                endGame();
            }
        }, 1000)
    }
}

function endGame() {
    if(timer !== undefined) {
        clearInterval(timer);
        timer = undefined;
    }

    if(points === Pokemon.length) {
        //Alle erraten
        alert("Du bist ein wahrer Pokemeister!");
    } else if(points <= 50) {
        alert(`Du hast ${points} von ${Pokemon.length} Pokemon erraten. Professor Eich wäre enttäuscht.`);
    } else if(points <= 100) {
        alert(`Du hast ${points} von ${Pokemon.length} Pokemon erraten. Du solltest dir den Pokedex nocheinmal anschauen!`);
    } else if(points <= 150) {
        alert(`Du hast ${points} von ${Pokemon.length} Pokemon erraten. Schon gut aber es geht noch besser!`);
    } else {
        alert(`Du hast ${points} von ${Pokemon.length} Pokemon erraten. Jeder macht mal Fehler. Guter Versuch!`);
    }

    toggleElements(false);
    completeMissingPokemons();
}

function completeMissingPokemons() {
    for(var i = 0; i < Pokemon.length; i++) {
        if(Pokemon[i].Found === undefined || !Pokemon[i].Found) {
            let elem = document.getElementById(Pokemon[i].Deutsch);
            elem.innerText = `${Pokemon[i].Nummer}. ${Pokemon[i].Deutsch}`;
            elem.style.color = "#f00";
        }
    }
}

function resetGame() {
    //Punkte resetten
    points = 0;

    for(var i = 0; i < Pokemon.length; i++) {
        let elem = document.getElementById(Pokemon[i].Deutsch);
        elem.innerText = Pokemon[i].Nummer + ".";
        elem.style.color = "#000";
        Pokemon[i].Found = false;
    }
}

function toggleElements(gameStarted) {
    //Elemente tauschen
    document.getElementById('playbutton').hidden = gameStarted;
    document.getElementById('inputlabel').hidden = !gameStarted;
    document.getElementById('inputfield').hidden = !gameStarted;
    document.getElementById('giveup').hidden = !gameStarted;

    let timer = document.getElementById('timer');
    let pointElem = document.getElementById('points');

    if(gameStarted) {
        timer.style.marginTop = "0px";
        pointElem.style.marginTop = "0px";

    } else {
        timer.style.marginTop = "0.2em";
        pointElem.style.marginTop = "0.5em";
    }

}
